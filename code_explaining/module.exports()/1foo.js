// module.exports to wbudowana właściwość obiektu module w Node.js, która jest używana do eksportowania elementów z pliku lub modułu do innych plików lub modułów. 
// Możesz użyć module.exports do eksportowania różnych elementów, takich jak funkcje, zmienne, obiekty itp.

// Eksportujemy funkcję sum
module.exports = function sum(a, b) {
    return a + b;
  };